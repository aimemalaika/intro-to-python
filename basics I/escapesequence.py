# to escape just add a back slash in before the element 
weather = "John said \"It is sunday and hot !\" is it real ?"

print(weather)

# \t will add a tab
weather2 = "\t John said \"It is sunday and hot !\" i\\s it real ?"
print(weather2)

# \n will add a new line
weather3 = "John said \"It is sunday and hot !\" i\\s it real ? \n"
print(weather3)